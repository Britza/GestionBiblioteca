    private void bRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bRegistrarActionPerformed
            String email = null;
            String direccion = null;
            String telefono =null;
            String titulo = tfTitulo.getText();
            String isbn = tfIsbn.getText();
            String nombre = tfNombre.getText();
            String apellidos = tfApellidos.getText();
            String dni = tfDni.getText();
            String codigo =tfCodigo.getText();
            String autor =null;
            String genero =null;
            String idioma =null;
            int unidades =0;
       
        int anio=dcFechaPrestamo.getCalendar().get(Calendar.YEAR);
        int mes=dcFechaPrestamo.getCalendar().get(Calendar.MONTH);
        int dia=dcFechaPrestamo.getCalendar().get(Calendar.DAY_OF_MONTH);
        String fechaPrestamo=""+anio+"-"+mes+"-"+dia;
         try {
           con = DriverManager.getConnection("jdbc:mysql://localhost:3306/gestiondebiblioteca","root","");
           Statement stmt = con.createStatement();
           
            ResultSet rs =stmt.executeQuery("select Unidades FROM libros WHERE ISBN='"+isbn+"'");
            if(rs.next()){
            int u = rs.getInt("Unidades");
            if(u==0){
                Metodos.mostrarPorVentana("No hay undidades disponibles");
            }else{
            stmt.executeUpdate("UPDATE libros SET Unidades =(Unidades-1) WHERE ISBN='"+isbn+"'");
            stmt.executeUpdate("INSERT INTO prestamos VALUES('"+titulo+"','"+isbn+"','"+fechaPrestamo+"','"+nombre+
            "','"+tfApellidos.getText()+"','"+tfDni.getText()+"','"+codigo+"')");
           Metodos.mostrarPorVentana("Se ha registrado correctamente");

           tfDni.setText("");
           tfNombre.setText("");
           tfApellidos.setText("");
           tfIsbn.setText("");
           tfNombre.setText("");
           dcFechaPrestamo.setDate(null);
           tfTitulo.setText("");
           tfCodigo.setText("");
            }
            }
            ResultSet rs1 =stmt.executeQuery("SELECT Email, Direccion, Telefono FROM usuarios WHERE DNI='"+dni+"'");
            if(rs1.next()){
                email = rs1.getString("Email");
                direccion =rs1.getString("Direccion");
                telefono = rs1.getString("Telefono");
            }
            ResultSet rs2 = stmt.executeQuery("SELECT Autor, Genero,Idioma,Unidades FROM libros WHERE ISBN='"+isbn+"'");

            if (rs2.next()){
                autor = rs2.getString("Autor");
                genero = rs2.getString("Genero");
                idioma = rs2.getString("Idioma");
                unidades =rs2.getInt("Unidades");
                
            }
            Socios socio =new Socios(nombre,apellidos,dni,email,direccion,telefono);
            Libros libro = new Libros(titulo,isbn,autor,genero,idioma,unidades);
            LibrosPrestados al= new LibrosPrestados(fechaPrestamo,codigo,libro,socio);
            prestamos.add(al);
            /*for(LibrosPrestados ele : prestamos){
                System.out.print(ele);
            }
            */
           Escritura.BorrarFichero("rexistroPrestamos");
           Escritura.escribirObxetos("rexistroPrestamos", prestamos); 
           
          
         


          } catch (SQLException ex) {
            Logger.getLogger(InsertarPrestamo.class.getName()).log(Level.SEVERE, null, ex);
            Metodos.mostrarPorVentana("No se puede conectar a la base de datos");
    
        }   
    }//GEN-LAST:event_bRegistrarActionPerformed

